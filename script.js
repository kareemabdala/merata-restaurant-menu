        // Add to cart
        function addToCart(name, price) {
            const existingItem = cart.find(item => item.name === name);
            
            if (existingItem) {
                existingItem.quantity += 1;
            } else {
                cart.push({ name, price, quantity: 1 });
            }
            
            localStorage.setItem('cart', JSON.stringify(cart));
            updateCartDisplay();
            showSuccessMessage();
        }

        // Update cart display
        function updateCartDisplay() {
            const cartItems = document.getElementById('cart-items');
            const cartCount = document.getElementById('cart-count');
            const cartTotal = document.getElementById('cart-total');
            const checkoutBtn = document.getElementById('checkout-btn');
            const totalAmount = document.getElementById('total-amount');

            const totalItems = cart.reduce((sum, item) => sum + item.quantity, 0);
            const totalPrice = cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);

            cartCount.textContent = totalItems;

            if (cart.length === 0) {
                cartItems.innerHTML = `<div class="empty-cart"><p>${translations[currentLang].emptyCart}</p></div>`;
                cartTotal.style.display = 'none';
                checkoutBtn.style.display = 'none';
            } else {
                const clearAllBtn = `
                    <div class="clear-all-container">
                        <button class="clear-all-btn" onclick="clearAllCart()">
                            ${translations[currentLang].clearAll}
                        </button>
                    </div>
                `;
                
                cartItems.innerHTML = clearAllBtn + cart.map(item => `
                    <div class="cart-item">
                        <div class="cart-item-info">
                            <div class="cart-item-name">${item.name}</div>
                            <div class="cart-item-price">${item.price} ${translations[currentLang].sar}</div>
                        </div>
                        <div class="cart-item-controls">
                            <button class="quantity-btn" onclick="updateQuantity('${item.name}', -1)">-</button>
                            <span class="quantity-display">${item.quantity}</span>
                            <button class="quantity-btn" onclick="updateQuantity('${item.name}', 1)">+</button>
                            <button class="remove-item-btn" onclick="removeFromCart('${item.name}')">${translations[currentLang].removeItem}</button>
                        </div>
                    </div>
                `).join('');
                
                totalAmount.textContent = totalPrice;
                cartTotal.style.display = 'block';
                checkoutBtn.style.display = 'block';
                checkoutBtn.textContent = translations[currentLang].completeOrder;
            }
        }

        // Update quantity
        function updateQuantity(name, change) {
            const item = cart.find(item => item.name === name);
            if (item) {
                item.quantity += change;
                if (item.quantity <= 0) {
                    removeFromCart(name);
                } else {
                    localStorage.setItem('cart', JSON.stringify(cart));
                    updateCartDisplay();
                }
            }
        }

        // Remove from cart
        function removeFromCart(name) {
            cart = cart.filter(item => item.name !== name);
            localStorage.setItem('cart', JSON.stringify(cart));
            updateCartDisplay();
        }

        // Clear all cart items with professional confirmation
        function clearAllCart() {
            showConfirmationModal(
                translations[currentLang].confirmClearTitle,
                translations[currentLang].confirmClearMessage,
                translations[currentLang].confirmClearSubMessage,
                () => {
                    cart = [];
                    localStorage.setItem('cart', JSON.stringify(cart));
                    updateCartDisplay();
                }
            );
        }

